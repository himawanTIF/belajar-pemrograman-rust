fn main() {
    println!("Hello, world!");
    println!("How");
    println!("Are");
    println!("You?");
}
// Pembuatan fungsi di rust menggunakan keyword fn
// Notasi pemanggilan nama_fungsi();
// Contoh: sebuah_fungsi("sebuah argument");
// Pemanggilan fungsi dengan macro bernama println! bukan fungsi println
// Pemanggilan macro ini ditandai dengan sintaksis !
// Penulisan statement wajib diakhiri tanda ;
// Standard indentasi untuk kode adalah 4 karakter spasi